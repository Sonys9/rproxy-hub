pub trait Color {
    fn with_place(&self, place: ColorPlace) -> String;
}

impl Color for str {
    fn with_place(&self, place: ColorPlace) -> String {
        self.replace("%place%", &(place as u8).to_string())
    }
}

pub enum ColorPlace {
    Foreground = 38,
    Background = 48,
}

pub fn find(name: &str) -> Option<&str> {
    COLORS
        .iter()
        .find(|&&color| color.0 == name)
        .map(|&color| color.1)
}

pub fn from_rgb(r: u8, g: u8, b: u8) -> String {
    format!("\x1b[%place%;2;{};{};{}m", r, g, b)
}

